    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        Cheque c = setCheque();
        ChequeStatus cs = null;
        if (realisedCheckBox.isSelected() != realisedByDefault) {
            cs = setChequeStatus();
        }

        if (!numberFlag) {
            displayMessage("Enter cheque number");
        } else if (!bankFlag) {
            displayMessage("Enter bank");
        } else if (!issuedDateFlag) {
            displayMessage("Enter issued date");
        } else if (!realDateFlag) {
            displayMessage("Enter realization date");
        } else {
            boolean chequeUpdated = false, chequeStatusUpdated = false;

            if (type.equals("R")) {
                recievedChequeController = new RecievedChequeController();
                recievedChequeStatusController = new RecievedChequeStatusController();

                try {
                    chequeUpdated = recievedChequeController.updateCheque(c);
                    if (realisedCheckBox.isSelected() != realisedByDefault) {
                        chequeStatusUpdated = recievedChequeStatusController.updateChequeStatus(cs);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(EditCheque.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(EditCheque.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else if (type.equals("I")) {
                issuedChequeController = new IssuedChequeController();
                issuedChequeStatusController = new IssuedChequeStatusController();

                try {
                    chequeUpdated = issuedChequeController.updateCheque(c);
                    if (realisedCheckBox.isSelected() != realisedByDefault) {
                        chequeStatusUpdated = issuedChequeStatusController.updateChequeStatus(cs);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(EditCheque.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(EditCheque.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

            if (chequeUpdated && chequeStatusUpdated) {
                dispose();
            } else {
                displayMessage("Cheque was not updated");
            }
        }
    }//GEN-LAST:event_saveButtonActionPerformed

