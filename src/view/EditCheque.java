    private void displayMessage(String message) {
        final String msg = message;

        new Thread() {
            public void run() {
                infoLabel.setText("");
                try {
                    sleep(50);
                } catch (InterruptedException ex) {
                }

                infoLabel.setText(msg);
                try {
                    sleep(50);
                } catch (InterruptedException ex) {
                }
                infoLabel.setText("");
                try {
                    sleep(100);
                } catch (InterruptedException ex) {
                }

                infoLabel.setText(msg);

                try {
                    sleep(5000);
                } catch (InterruptedException ex) {
                }

                infoLabel.setText("");
            }
        }.start();
    }

    private void setDetails() {
        String iDate, rDate;

        chID = cheque.getId();
        type = chID.charAt(1) + "";

        idLabel.setText(chID);
        orderIDLabel.setText(cheque.getOrderId());
        amountLabel.setText(cheque.getAmount() + "");
        numberText.setText(cheque.getNumber());
        bankText.setText(cheque.getBank());
        iDate = cheque.getIssuedDate();
        rDate = cheque.getRealDate();
        try {
            issuedDateText.setDate(dateFormat.parse(iDate));
            realDateText.setDate(dateFormat.parse(rDate));
        } catch (ParseException ex) {
        }

        if (type.equals("R")) {
            recievedChequeStatusController = new RecievedChequeStatusController();

            try {
                chequeStatusesList = recievedChequeStatusController.searchChequeStatuses(chID, "chequeId");
            } catch (SQLException ex) {
                Logger.getLogger(EditCheque.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(EditCheque.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (type.equals("I")) {
            issuedChequeStatusController = new IssuedChequeStatusController();

            try {
                chequeStatusesList = issuedChequeStatusController.searchChequeStatuses(chID, "chequeId");
            } catch (SQLException ex) {
                Logger.getLogger(EditCheque.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(EditCheque.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

        ChequeStatus cs = chequeStatusesList.get(0);
        csID = cs.getId();
        if (cs.getStatus().equals("done")) {
            realisedByDefault = true;
            realisedCheckBox.setSelected(true);
        }
    }

    private Cheque setCheque() {
        String id, orderID, number, bank, issuedDate = null, realDate = null;
        double amount;
        Date iDate, rDate;

        id = idLabel.getText();
        orderID = orderIDLabel.getText();
        number = numberText.getText();
        if (!number.equals("")) {
            numberFlag = true;
        }
        bank = bankText.getText();
        if (!bank.equals("")) {
            bankFlag = true;
        }
        iDate = issuedDateText.getDate();
        if (iDate != null) {
            issuedDateFlag = true;
            issuedDate = dateFormat.format(iDate);
        }
        rDate = realDateText.getDate();
        if (rDate != null) {
            realDateFlag = true;
            realDate = dateFormat.format(rDate);
        }
        amount = Double.parseDouble(amountLabel.getText());

        Cheque c = new Cheque(id, orderID, number, bank, issuedDate, realDate, amount);

        return c;
    }

    private ChequeStatus setChequeStatus() {
        String status;
        if (realisedCheckBox.isSelected()) {
            status = "done";
        } else {
            status = "pending";
        }
        ChequeStatus cs = new ChequeStatus(csID, idLabel.getText(), status);

        return cs;
    }
