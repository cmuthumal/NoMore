    private void setTable() {
        tableModel.setRowCount(0);
        if (!employeesList.isEmpty()) {
            int phone;
            for (int i = 0; i < employeesList.size(); i++) {
                Employee emp = employeesList.get(i);
                lastID = emp.getId();
                phone = emp.getPhone();
                if (phone == 0) {
                    Object[] row = {emp.getId(), emp.getName(), emp.getNic() + "", emp.getDob(), emp.getAddress(), emp.getSalary(), "0" + emp.getMobile(), null, emp.getJoinedDate(), emp.getNotes()};
                    tableModel.addRow(row);
                } else {
                    Object[] row = {emp.getId(), emp.getName(), emp.getNic() + "", emp.getDob(), emp.getAddress(), emp.getSalary(), "0" + emp.getMobile(), "0" + emp.getPhone(), emp.getJoinedDate(), emp.getNotes()};
                    tableModel.addRow(row);
                }
            }
        } else {
            showMainMessage("No employees in database");
            lastID = new Extra().getFirstID("employee");
        }
    }

