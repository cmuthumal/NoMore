    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        clearFieldsPanel();
        setFieldsPanel(false);
        boolean deleted = false;
        if (employeesTable.getSelectedRow() != -1) {
            int confirmation = JOptionPane.showConfirmDialog(null, "Delete selected employee?", "Confirm", JOptionPane.YES_NO_OPTION);
            if (confirmation == 0) {
                employeeController = new EmployeeController();
                String id = (String) employeesTable.getValueAt(employeesTable.getSelectedRow(), 0);

                try {
                    deleted = employeeController.deleteEmployee(id);
                } catch (SQLException ex) {
                    Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
                }

                if (deleted) {
                    showMainMessage("'" + id + "' has been deleted");
                    viewAllButtonActionPerformed(null);
                } else {
                    showMainMessage("Employee has not been deleted");
                }
            }
        } else {
            showMainMessage("Select an employee first");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

