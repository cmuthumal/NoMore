    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        Employee emp = setEmployee();

        if (!nameFlag) {
            showFieldsPanelMessage("Enter a name");
            nameText.requestFocus();
        } else if (!nicFlag) {
            showFieldsPanelMessage("Enter a valid NIC number");
            nicNoText.requestFocus();
        } else if (!addressFlag) {
            showFieldsPanelMessage("Enter an address");
            addressText.requestFocus();
        } else if (!salaryFlag) {
            showFieldsPanelMessage("Enter a salary");
            salaryText.requestFocus();
        } else if (!mobileFlag) {
            showFieldsPanelMessage("Enter a valid mobile number");
            mobileText.requestFocus();
        } else if (!phoneFlag) {
            showFieldsPanelMessage("Enter a valid phone number");
            phoneText.requestFocus();
        } else if (!joinedDateFlag) {
            showFieldsPanelMessage("Enter joined date");
            joinedDateText.requestFocus();
        } else {
            employeeController = new EmployeeController();

            if (mode == 1) {
                boolean updated = false;
                try {
                    updated = employeeController.updateEmployee(emp);
                } catch (SQLException ex) {
                    Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (updated) {
                    showMainMessage("Emplyee updated successfully");
                    clearFieldsPanel();
                    setFieldsPanel(false);
                    viewAllButtonActionPerformed(null);
                } else {
                    showMainMessage("Emplyee was not updated");
                }
            } else if (mode == 2) {
                boolean added = false;
                try {
                    added = employeeController.addEmployee(emp);
                } catch (SQLException ex) {
                    Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (added) {
                    showMainMessage("New emplyee added successfully");
                    clearFieldsPanel();
                    setFieldsPanel(false);
                    viewAllButtonActionPerformed(null);
                } else {
                    showMainMessage("New emplyee was not added");
                }
            }
        }
        setFlags(false);
    }//GEN-LAST:event_saveButtonActionPerformed

