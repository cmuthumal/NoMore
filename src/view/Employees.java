    private void setFieldValues(int selectedRow) {
        idLabel.setText((String) employeesTable.getValueAt(selectedRow, 0));
        nameText.setText((String) employeesTable.getValueAt(selectedRow, 1));
        nicNoText.setText((String) employeesTable.getValueAt(selectedRow, 2));

        try {
            Date date = dateFormat.parse((String) employeesTable.getValueAt(selectedRow, 3));
            dobText.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
        }

        addressText.setText((String) employeesTable.getValueAt(selectedRow, 4));
        salaryText.setText(employeesTable.getValueAt(selectedRow, 5) + "");
        mobileText.setText(employeesTable.getValueAt(selectedRow, 6) + "");
        if (employeesTable.getValueAt(selectedRow, 7) != null) {
            phoneText.setText(employeesTable.getValueAt(selectedRow, 7) + "");
        }

        try {
            Date date = dateFormat.parse((String) employeesTable.getValueAt(selectedRow, 8));
            joinedDateText.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
        }

        notesText.setText((String) employeesTable.getValueAt(selectedRow, 9));
    }

    private Employee setEmployee() {
        String name = "", dob = "", address = "", joinedDate = "", note = "";
        int nic = 0, mobile = 0, phone = 0;
        double salary = 0;
        Employee em = null;

        if (!nameText.getText().equals("")) {
            nameFlag = true;
            name = nameText.getText();
        }
        if (!nicNoText.getText().equals("")) {
            nic = Integer.parseInt(nicNoText.getText());
            if (new Extra().validateNICNumber(nicNoText)) {
                nicFlag = true;
            }
        }
        if (dobText.getDate() != null) {
            joinedDateFlag = true;
            Date d = dobText.getDate();
            dob = dateFormat.format(d);
        } else {
            dob = "";
        }
        if (!addressText.getText().equals("")) {
            addressFlag = true;
            address = addressText.getText();
        }
        if (!salaryText.getText().equals("")) {
            salaryFlag = true;
            salary = Double.parseDouble(salaryText.getText());
        }
        if (!mobileText.getText().equals("")) {
            if (new Extra().validateMobileNumber(mobileText)) {
                mobileFlag = true;
                mobile = Integer.parseInt(mobileText.getText());
            }
        }
        if (!phoneText.getText().equals("")) {
            if (new Extra().validatePhoneNumber(phoneText)) {
                phoneFlag = true;
                phone = Integer.parseInt(phoneText.getText());
            }
        } else {
            phoneFlag = true;
        }
        if (joinedDateText.getDate() != null) {
            joinedDateFlag = true;
            Date d = joinedDateText.getDate();
            joinedDate = dateFormat.format(d);
        }
        if (!notesText.getText().equals("")) {
            note = notesText.getText();
        }

        em = new Employee(idLabel.getText(), name, nic, dob, address, salary, mobile, phone, joinedDate, note);

        return em;
    }

